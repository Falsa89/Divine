const RUNTIME_FRAME_WIDTH = 640;
const RUNTIME_FRAME_HEIGHT = 768;

function makeRuntimeSheet(
  source: ImageSourcePropType,
  frames: number,
  columns: number,
  fps: number,
  loop: boolean,
) {
  return {
    source,
    frames,
    columns,
    rows: 1,
    fps,
    loop,
    frameWidth: RUNTIME_FRAME_WIDTH,
    frameHeight: RUNTIME_FRAME_HEIGHT,
    visualScale: 1.0,
  };
}

